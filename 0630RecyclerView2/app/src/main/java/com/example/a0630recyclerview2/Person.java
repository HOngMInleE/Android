package com.example.a0630recyclerview2;

public class Person {
    String name;
    String moblile;

    public Person(String name, String moblile) {
        this.name = name;
        this.moblile = moblile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMoblile() {
        return moblile;
    }

    public void setMoblile(String moblile) {
        this.moblile = moblile;
    }
}
